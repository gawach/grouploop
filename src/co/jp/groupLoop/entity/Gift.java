package co.jp.groupLoop.entity;

public class Gift {
	private int id_gift;
	private String name_gift;
	private int point_gift;
	private String image_gift;

	public int getId_gift() {
		return id_gift;
	}

	public void setId_gift(int id_gift) {
		this.id_gift = id_gift;
	}

	public String getName_gift() {
		return name_gift;
	}

	public void setName_gift(String name_gift) {
		this.name_gift = name_gift;
	}

	public int getPoint_gift() {
		return point_gift;
	}

	public void setPoint_gift(int point_gift) {
		this.point_gift = point_gift;
	}

	public String getImage_gift() {
		return image_gift;
	}

	public void setImage_gift(String image_gift) {
		this.image_gift = image_gift;
	}
}
